document.getElementById("content_list").onmouseover=function(event){
    let target = event.target;
    if (target.className == "title") {
        let s = getElementByClassName("title01");
        closeMenu();
        s[0].style.display="block";
    }
}
document.onmouseover=function (event) {
    let target = event.target;
    console.log=event.target;
    if (target.className!="title"&& target.className!="title01") {
        closeMenu();
    }
}
function closeMenu(){
   
    let submenu = document.getElementsByClassName("title01");
    for (let i = 0; i < submenu.length; i++) {
        submenu[i].style.display = "none";
        
    }
}



function readMore(){
    let dots = document.getElementById("dots");  
    let more = document.getElementById("more");
    let btn = document.getElementById("btn");
    let content =document.getElementById ("content");
    let nav_content = document.getElementById("nav_content");
    let btn01 = document.getElementById("btn01");
    let btn02 = document.getElementById("btn02");
    let title=document.getElementById("title");
    
  

if (dots.style.display === "none") {
    nav_content.style.display = "flex";  
    dots.style.display ="inline";
    btn.innerHTML="read more";
    more.style.display="none";
    content.style.width ="500px";
    title.style.paddingTop ="80px"
    btn01.style.display="none";
   
    btn02.style.display="none";

    
}else{
    dots.style.display ="none";
    nav_content.style.display = "block";
    nav_content.style.textAlign ="justify";
    btn.innerHTML="hide";
    more.style.display="inline";
    content.style.width ="800px";
    content.style.paddingLeft ="40px";
    title.style.paddingTop ="20px";
    btn01.style.display="inline";  
    btn02.style.display="inline";
   

}
}
